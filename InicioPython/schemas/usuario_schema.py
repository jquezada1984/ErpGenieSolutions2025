from marshmallow import Schema, fields

class UsuarioSchema(Schema):
    id_usuario = fields.Str(dump_only=True)
    id_empresa = fields.Str(required=True)
    id_perfil = fields.Str(required=True)
    username = fields.Str(required=True)
    password = fields.Str(load_only=True)  # opcional: si viene, se hashea; si no, usar password_hash
    password_hash = fields.Str(load_only=True)
    nombre_completo = fields.Str()
    email = fields.Str()
    estado = fields.Bool()
    created_at = fields.DateTime(dump_only=True)
    updated_at = fields.DateTime(dump_only=True)
    # Dolibarr
    titulo_cortesia = fields.Str()
    apellidos = fields.Str()
    nombre = fields.Str()
    administrador_sistema = fields.Bool()
    sexo = fields.Str(allow_none=True)
    es_empleado = fields.Bool()
    id_supervisor = fields.Str()
    id_validador_gastos = fields.Str()
    id_validador_dias_libres = fields.Str()
    usuario_externo = fields.Bool()
    fecha_validez_desde = fields.Date()
    fecha_validez_hasta = fields.Date()
    direccion = fields.Str()
    codigo_postal = fields.Str()
    poblacion = fields.Str()
    id_pais = fields.Str()
    id_provincia = fields.Str()
    telefono_trabajo = fields.Str()
    movil = fields.Str()
    fax = fields.Str()
    codigo_contable = fields.Str()
    color = fields.Str()
    etiquetas_categorias = fields.Str()
    idioma_default = fields.Str()
    firma = fields.Str()
    nota_publica = fields.Str()
    nota_privada = fields.Str()
    puesto_trabajo = fields.Str()
    tasa_hora = fields.Decimal(as_string=True)
    tasa_dia = fields.Decimal(as_string=True)
    salario = fields.Decimal(as_string=True)
    horas_semana = fields.Decimal(as_string=True)
    fecha_empleo_desde = fields.Date()
    fecha_empleo_hasta = fields.Date()
    fecha_nacimiento = fields.Date() 